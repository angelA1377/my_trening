def generate_passwords(n):
    i = 1  # Начальное значение i
    found = False  # Флаг для отслеживания найденных паролей

    # Генерация пар чисел с помощью цикла while
    while i < 21:  # Цикл пока i меньше или равно 20
        j = i + 1  # Установка начального значения j
        while j < 21:  # Цикл пока j меньше или равно 20
            if n % (i + j) == 0:
                print(f"{i}{j}", end='')  # Выводим пароль
                found = True  # Обновляем флаг, что хотя бы один пароль найден
            j += 1  # Увеличиваем j на 1
        i += 1  # Увеличиваем i на 1

    if not found:
        print("Нет доступных паролей для числа", n)


# Пример использования
n = int(input("Введите число от 3 до 20: "))
if 3 <= n <= 20:
    print(f"Пароли для числа {n}: ", end='')
    generate_passwords(n)
else:
    print("Пожалуйста, введите число в диапазоне от 3 до 20.")